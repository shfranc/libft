#include "libft.h"

uint64_t		ft_swap_uint64(uint64_t n)
{
	uint64_t	swap;

	swap = ((n >> 56) & 0xff)
		| ((n >> 40) & 0xff00)
		| ((n >> 24) & 0xff0000)
		| ((n >> 8) & 0xff000000)
		| ((n << 8) & 0xff00000000)
		| ((n << 24) & 0xff0000000000)
		| ((n << 40) & 0xff000000000000)
		| ((n << 56) & 0xff00000000000000);
	return (swap);
}
