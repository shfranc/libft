#include "libft.h"

uint32_t		ft_swap_uint32(uint32_t n)
{
	uint32_t	swap;

	swap = ((n >> 24) & 0xFF)
		| ((n >> 8) & 0xFF00)
		| ((n << 8) & 0xFF0000)
		| ((n << 24) & 0xFF000000);
	return (swap);
}
