#include "libft.h"

unsigned int     ft_swap_uint32(unsigned int n)
{
    int     swap;

    swap = ((n >> 24) & 0xFF)\
        | ((n >> 8) & 0xFF00)\
        | ((n << 8) & 0xFF0000)\
        | ((n << 24) & 0xFF000000);
    return (swap);
}
